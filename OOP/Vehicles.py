class Vehicles:

    vehicleTypeName = ""
    numberOfDoors = 0 
    vehicleType = ""

    def vehicleType(vehicleType):
        vehicleTypeName =  vehicleType
        return vehicleTypeName

    def hasDoors(vehicleTypeName):
        if vehicleTypeName == "Bikes":
            return False
        else:
            return True
    
    def setNumberOfdoors(vehicleTypeName):
        if vehicleTypeName == "Bikes":
            numberOdoors = 0
        elif vehicleTypeName == "Buses" | vehicleTypeName == "Trucks":
            numberOdoors = 2
        else:
            numberOdoors = 4
        return numberOdoors

    def setVehicleType(vehicleType):
        if vehicleType == "":
            vehicleType = vehicleType

    def getVehicleType(self):
        if self.vehicleType != "":
            return self.vehicleType

    def getNumberOfDoors():
        return numberOdoors

    


    